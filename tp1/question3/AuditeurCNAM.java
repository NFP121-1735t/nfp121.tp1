public class AuditeurCNAM {
    /** l'attribut nom de chaque auditeur. */
    private String nom;
    /** l'attribut prenom de chaque auditeur. */
    private String prenom;
    /** l'attribut matricule de chaque auditeur. */
    private String matricule;

    /**
     * "Création", le constructeur d'un auditeur avec son nom, son prénom et son
     * matricule.
     * 
     * @param nom
     *            le nom de l'auditeur
     * @param prenom
     *            son prénom
     * @param matricule
     *            sur la carte d'inscription, près de la photo
     */
    public AuditeurCNAM(String nom, String prenom, String matricule) {
        this.nom = nom;
        this.prenom = prenom;
        this.matricule = matricule;
    }

    /**
     * le login au Cnam : 6 premières lettres du nom suivies de la première
     * lettre du prénom séparées de '_' . le login retourné est en minuscules,
     * le trait d'union, ou spéciaux <i>(pour unix)</i> sont remplacés par des
     * '_' pas de caractères accentués pour le login voir les classes
     * prédéfines, java.lang.String : les méthodes replaceAll, toLowerCase et
     * substring java.lang.Math : la méthode min<br>
     * <b>BlueJ : Menu Aide</b>
     * 
     * @return le login du Cnam simplifié, sans les adaptations dues aux
     *         homonymes...
     */
    public String login() {
        String n = this.nom;
        String p = this.prenom;
       
        if(n.length()>6){ // recuperer la nom qui a 6 ou plus lettres
            n=n.substring(0,6);//enlever le 6 premiers lettres du nom
        }
        n=n.replaceAll(" " , "_");
        n=n.replaceAll("-","_");
        p=p.substring(0,1); // enlever la premier lettre du prenom
        

    String fini ;
    fini= n+ "_" +p; // camcatenation du 6 lettres avec la lettre du prenom
     
        fini=fini.replaceAll("[é,è,ê]" , "e");//remplacer l'accent par une 'e' sans accent
        fini=fini.replaceAll("[î,ï,ì]" , "i");//remplace l"i" accent par une "i" sans accent
        fini=fini.replaceAll("[Ç,ç]" , "c");//remplacer le "C" cedille par une "C" sans cedille
        fini=fini.replaceAll("[â,à,Ä,Å]" , "a");//remplacer l"a" accent par une"A" sans accent
        fini=fini.toLowerCase(); // transformer la username en miniscule 
   
        return fini;// rendre la final resultat
    }

    /**
     * Lecture du nom de l'auditeur.
     * 
     * @return son nom
     */
    public String nom() {
      
        String nomAuditeur=this.nom; 
        
        return nomAuditeur;// à compléter
    }

    /**
     * Lecture du prénom de l'auditeur.
     * 
     * @return son prénom
     */
    public String prenom() {
        String prenomAuditeur=this.prenom;
        return prenomAuditeur;
        // à compléter
    }

    /**
     * Lecture du matricule de l'auditeur.
     * 
     * @return son matricule
     */
    public String matricule() {
        String matriculeAuditeur=this.matricule;

        return matriculeAuditeur;// à compléter
    }

    /**
     * méthode toString ( méthode redéfinie de la classe Object).
     * 
     * @return la concaténation du nom, du prénom et du login, selon cette
     *         syntaxe
     *         <code>nom() + " " + prenom() +  " login : " + login()</code>
     */
    @Override
    public String toString() {
        return nom() + " " + prenom() + " login : " + login();
    }

}
